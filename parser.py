def parse_publication_date(date_text):
    """–ü–∞—Ä—Å–∏—Ç —Ç–µ–∫—Å—Ç –¥–∞—Ç—ã –≤ datetime –æ–±—ä–µ–∫—Ç"""
    now = datetime.now()
    
    # –°–µ–≥–æ–¥–Ω—è
    if '—Å–µ–≥–æ–¥–Ω—è' in date_text.lower():
        time_match = re.search(r'(\d{1,2}):(\d{2})', date_text)
        if time_match:
            hour, minute = int(time_match.group(1)), int(time_match.group(2))
            return now.replace(hour=hour, minute=minute, second=0, microsecond=0)
    
    # –í—á–µ—Ä–∞
    elif '–≤—á–µ—Ä–∞' in date_text.lower():
        time_match = re.search(r'(\d{1,2}):(\d{2})', date_text)
        if time_match:
            hour, minute = int(time_match.group(1)), int(time_match.group(2))
            yesterday = now - timedelta(days=1)
            return yesterday.replace(hour=hour, minute=minute, second=0, microsecond=0)
    
    # –ö–æ–Ω–∫—Ä–µ—Ç–Ω–∞—è –¥–∞—Ç–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, "12 —è–Ω–≤")
    elif re.search(r'\d{1,2}\s+[–∞-—è]+', date_text.lower()):
        # –£–ø—Ä–æ—â—ë–Ω–Ω—ã–π –ø–∞—Ä—Å–∏–Ω–≥ - —Å—á–∏—Ç–∞–µ–º —á—Ç–æ —ç—Ç–æ –Ω–µ —Å–µ–≥–æ–¥–Ω—è
        return now - timedelta(days=2)
    
    return None

